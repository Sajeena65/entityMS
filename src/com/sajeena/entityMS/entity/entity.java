package com.sajeena.entityMS.entity;

/**
 *
 * @author Admin
 */
public class entity {
      private int id;
    private String entityname,description;
    private boolean status;

    public entity() {
    }

    public entity(int id, String entityname, String description, boolean status) {
        this.id = id;
        this.entityname = entityname;
        this.description = description;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEntityname() {
        return entityname;
    }

    public void setEntityname(String entityname) {
        this.entityname = entityname;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
}
